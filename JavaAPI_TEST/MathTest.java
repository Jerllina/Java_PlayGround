package JavaAPItest;

public class MathTest {
	public static void main(String[] args) {
		//绝对值
        System.out.println(Math.abs(-12.7));
        //大于等于它的最小整数值
        System.out.println(Math.ceil(12.7));
        //最接近的整数值
        System.out.println(Math.rint(12.4));
        //返回0到1之间的double值
        System.out.println(Math.random());
	}
}
